#include "minishell.h"

void	cmd_echo(char **arg, t_main *main)
{
	int	i;
	
	(void)main;
	i = 0;
	if (arg[0][0] == '-' && arg[0][1] == 'n' && !arg[0][2])
		i++;
	while (arg[i])
		printf("%s", arg[i++]);
	if (!(arg[0][0] == '-' && arg[0][1] == 'n' && !arg[0][2]))
		printf("\n");
}
