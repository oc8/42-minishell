#include "minishell.h"

int	cmd_echo(char **arg)
{
	int	i;
	
	i = 0;
	if (arg[0] == "-n")
		i++;
	while (arg[i])
		printf("%s\n", arg[i++]);
	if (arg[0] == "-n")
		printf("\n");
	return (0);
}
